

function TIC()
  t=time() / 1500
  for x=0,239 do
   for y=0,135 do
   	x1=x-60+math.sin(t/20)*120
    y1=y-34 +math.sin(t/20)*68
    op=1
    u=math.cos(t)*x1+math.sin(t)*y1*op
    v=math.cos(t)*y1-math.sin(t)*x1*op
    a1=math.cos(t*10)*1.1 
    a2=math.cos(t*10)*1.1
    c=math.sin(u/(20+a1))+math.sin(v/(20+a2))-8*(t/10)
    pix(x,y,c*8)
   end
  end

end

